from math import radians, sin, cos, tan
n1 = float(input('Digite um 창ngulo qualquer: '))
seno = sin(radians(n1))
print('O 창ngulo de ', n1, 'tem o seno de', seno)
cosseno = cos(radians(n1))
print('O 창ngulo de ', n1, 'tem o cosseno de', cosseno)
tangente = tan(radians(n1))
print('O 창ngulo de ', n1, 'tem a tangente de', tangente)